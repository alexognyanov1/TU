-- Задача 2: SELECT с ограничаващо условие
-- ============================================================
-- Извежда всички активни потребители, регистрирани след 2020 г.,
-- чиито профили са прегледани поне веднъж, сортирани по дата.
-- ============================================================

USE messaging_system;

SELECT
    user_id,
    CONCAT(first_name, ' ', last_name) AS full_name,
    username,
    email,
    phone,
    created_at
FROM users
WHERE
    is_active = 1
    AND created_at >= '2020-01-01'
ORDER BY created_at DESC;
