USE twitter_db;

-- ------------------------------------------------------------
-- Заявка: Извеждане на всички активни (неизтрити) туити
-- от активни потребители, чието съдържание е по-дълго от 40 знака,
-- публикувани след 01.02.2026 г., наредени по дата (най-нови първи).
-- ------------------------------------------------------------
SELECT
    t.id         AS tweet_id,
    u.username,
    u.first_name,
    u.last_name,
    t.content,
    t.created_at
FROM tweets t
JOIN users u ON t.user_id = u.id
WHERE t.is_deleted = 0
  AND u.is_active  = 1
  AND LENGTH(t.content) > 40
  AND t.created_at >= '2026-02-01 00:00:00'
ORDER BY t.created_at DESC;
